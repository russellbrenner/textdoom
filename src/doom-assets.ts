/**
 * Get the appropriate face based on health and state
 */
export function getDoomFace(
  health: number,
  state: 'idle' | 'hurt_left' | 'hurt_right' | 'grin' = 'idle',
  godMode: boolean = false
): { lines: string[]; color: string } {
  if (godMode) {
    return { lines: FACE_GOD, color: '#ffff00' };
  }

  if (health <= 0) {
    return { lines: FACE_DEAD, color: '#880000' };
  }

  let faceSet: Record<string, string[]>;
  let color: string;

  if (health > 60) {
    faceSet = FACE_HEALTHY;
    color = '#ffcc88';
  } else if (health > 20) {
    faceSet = FACE_HURT;
    color = '#ff8866';
  } else {
    faceSet = FACE_DYING;
    color = '#ff4444';
  }

  return {
    lines: faceSet[state] || faceSet.idle,
    color,
  };
}

